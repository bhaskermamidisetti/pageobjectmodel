package com.affluence.java.util;

public class CurrentStepDataBean {
	
	private String stepDescription;
	private String stepResult;
	private ImageDetailsBean imageDetailsBean;
	public String getStepDescription() {
		return stepDescription;
	}
	public void setStepDescription(String stepDescription) {
		this.stepDescription = stepDescription;
	}
	public String getStepResult() {
		return stepResult;
	}
	public void setStepResult(String stepResult) {
		this.stepResult = stepResult;
	}
	public ImageDetailsBean getImageDetailsBean() {
		return imageDetailsBean;
	}
	public void setImageDetailsBean(ImageDetailsBean imageDetailsBean) {
		this.imageDetailsBean = imageDetailsBean;
	}
	
	
	

}
